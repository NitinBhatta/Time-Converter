// Declare the variable for minutes (example: 150 minutes)
let minutes = 150;

// Use operators to calculate the number of hours and remaining minutes
let hours = Math.floor(minutes / 60); // Get the whole number of hours
let remainingMinutes = minutes % 60; // Get the remaining minutes

// Output the result
console.log("Hours: " + hours);
console.log("Minutes: " + remainingMinutes);
